public class PairOfDigits {
    private  int arabic;
    private  char romen;

    public PairOfDigits(int arabic, char romen) {
        this.arabic = arabic;
        this.romen = romen;
    }

    public int getArabic() {
        return arabic;
    }

    public char getRomen() {
        return romen;
    }
}
